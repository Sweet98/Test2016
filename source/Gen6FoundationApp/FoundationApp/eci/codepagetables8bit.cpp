CTransInfo8Bit::CTransInfo8Bit(codepage_t codepage, const wchar_t *pSrc)
: CTransInfoBase(CP8_SIZE, codepage)
{
	m_capabilities = FL_8Bit;
	wchar_t	*pDest = m_pTable;

	// lower half is filled from base class with 1:1 translation
	// then fill in the rest from the table in ROM
	for(int i=CP8_SIZE/2; i<CP8_SIZE; i++)
	{
		pDest[i] = pSrc[i-CP8_SIZE/2];
	}
}

void CTransInfo8Bit::FromUnicode_(wchar_t In, bchar_t &Out)
{
	// values below 128 are 1:1
	if(In < CP7_SIZE)
	{
		Out = (bchar_t)In;
		return;	// speed
	}

	for (size_t i=CP7_SIZE; i<CP8_SIZE; i++)
	{
		if(m_pTable[i] == In)
		{
			Out = (bchar_t)i;
			return;
		}
	};
	Out = '?';
}

