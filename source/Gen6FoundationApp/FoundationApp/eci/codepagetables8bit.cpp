//////////////////////////////////////////////////////////////////////////////////////////
void	CTransCode::Create8BitTable(const TranslateItemUnicode_t * const pList)
{
	codepage_t codepage=pList->Codepage;
	ASSERT(m_TransInfo[codepage] == NULL);
	switch (pList->SrcTableSize)
	{
	case CP8_SIZE:  // some codepages are even different in the ASCII range
		m_TransInfo[codepage] = new CTransInfo8BitExotic(codepage, pList->pTable);
		break;
	case CP8_SIZE/2:
		m_TransInfo[codepage] = new CTransInfo8Bit(codepage, pList->pTable);
		break;
	default:
	case 0:			// 1:1 translation
		m_TransInfo[codepage] = new CTransInfo8Bit1To1(codepage);
		break;
	}
}

//////////////////////////////////////////////////////////////////////////////////////////
// Make all 8 bit tables
void	CTransCode::Create8BitTables(void)
{
	codepage_t codepage;
	// search through the list of supported codepages
	const TranslateItemUnicode_t *pList = All8BitCodePages;
	while ( pList->Codepage != CP_NOTSUPPORTED )
	{
		ASSERT(pList->Codepage < CP_MAX);
		codepage = pList->Codepage;
		ASSERT(codepage < CP_MAX);
		if(codepage < CP_MAX)
		{
			if (codepage != m_SystemCodepage)	// don't do it twice
			{
				if(m_TransInfo[codepage] == NULL)	// only create if not done yet (memory leak!)
					Create8BitTable(pList);
			}
		}
		pList++;
	}
}


// The system codepage is optimized to support both directions (from/to unicode) in a fast way.
void CTransCode::CreateSystemCPTable(void)
{
	ASSERT(m_SystemCodepage < CP_MAX);
	if(m_TransInfo[m_SystemCodepage] != NULL)
	{
		// only if we need to change it.
		if (m_SystemCodepage != m_TransInfo[m_SystemCodepage]->GetCurrentCodepage())
		{
			delete m_TransInfo[m_SystemCodepage];	   // delete old system codepage
			m_TransInfo[m_SystemCodepage] = NULL;
		}
	}

	if(m_TransInfo[m_SystemCodepage] == NULL)
	{
		const TranslateItemUnicode_t *pList = All8BitCodePages;

		while ( pList->Codepage != CP_NOTSUPPORTED )
		{
			ASSERT(pList->Codepage < CP_MAX);
			codepage_t codepage = pList->Codepage;
			ASSERT(codepage < CP_MAX);
			if(codepage < CP_MAX)
			{
				if (codepage == m_SystemCodepage)
				{
					// replace codepage translator with speed optimized version (needs 64KB more RAM!)
					delete m_TransInfo[codepage];  // cleanup old codepage translator
					m_TransInfo[codepage] = new CTransInfoSystemCP(codepage, pList->pTable, pList->SrcTableSize);
				}
				else
				{
					// in case we removed an old system codepage, we must recreate that codepage with normal attributes
					if (m_TransInfo[codepage] == NULL)
					{
						Create8BitTable(pList);
					}
				}
			}
			pList++;
		}
	}
}


