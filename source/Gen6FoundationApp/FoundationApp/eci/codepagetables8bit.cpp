// Handler for the exotix 8 Bit codepages where even the characters below 128 are different (arabic)
class CTransInfo8BitExotic : public CTransInfoBase
{
public:
	CTransInfo8BitExotic(codepage_t codepage, const wchar_t *pSrc);
protected:
	virtual MatrixError_t FromUnicode(const wchar_t *pIn, bchar_t *pOut, size_t &DestinationSize);
	virtual MatrixError_t FromUnicode(const wchar_t *pIn, size_t length, bchar_t *pOut, size_t &DestinationSize);
private:
	void FromUnicode_(wchar_t In, bchar_t &Out);
};

CTransInfo8BitExotic::CTransInfo8BitExotic(codepage_t codepage, const wchar_t *pSrc)
: CTransInfoBase(0, codepage)
{
	m_capabilities = FL_8Bit;
	m_pTable = (wchar_t *)pSrc;            // this is a kludge, pSrc is a pointer to const !!
	m_TableSize = CP8_SIZE;
}

void CTransInfo8BitExotic::FromUnicode_(wchar_t In, bchar_t &Out)
{
	for (size_t i=0; i<CP8_SIZE; i++)
	{
		if(m_pTable[i] == In)
		{
			Out = (bchar_t)i;
			return;
		}
	};
	Out = '?';
}

MatrixError_t CTransInfo8BitExotic::FromUnicode(const wchar_t *pIn, bchar_t *pOut, size_t &DestinationSize)
{
	bchar_t *pStart=pOut;
	while(*pIn != 0)
	{
		if (DestinationSize-- == 0)
			return ERR_INSUFFIZIENT_BUFFER;
		FromUnicode_(*pIn++,*pOut++);
	};
	DestinationSize = pOut-pStart;
	return ERR_PASS;
}

MatrixError_t CTransInfo8BitExotic::FromUnicode(const wchar_t *pIn, size_t length, bchar_t *pOut, size_t &DestinationSize)
{
	if(DestinationSize < length)
		return ERR_INSUFFIZIENT_BUFFER;
	DestinationSize = length;	            // return final size
	for (size_t i=0; i<length; i++)
	{
		FromUnicode_(*pIn++,*pOut++);
	};
	return ERR_PASS;
}

