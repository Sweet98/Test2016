/////////////////////////////////////////////////////////////////////////////////////////
// Helper for the DOS and DEC tables

keynum_t CWedgeBase::CntrlToKeynumber(const CntrlKeynumbers *cntrlkey, tichar_t ascii)
{
	if ('\0' == ascii)
		return 3;												/* same with all terminals/countries */

	if (ascii <= 'Z'-'@')
        return m_KeyNumLookup.GetKeyNumber( (tichar_t)(ascii+'a'-1) );     /* same with all terminals/countries */

	return cntrlkey->key[ascii-('Z'-'@'+1)];			/* special table neccessary			 */
}

keynum_t CWedgeBase::GetCntrlPlusASCIIKeynumber(tichar_t ascii)
{
	const CntrlKeynumbers *cntrlkey;
	int country;
	if (m_pTerminal->IsCapCntrlDOS())
	{
		country = GetCountry();
		if (country > MAX_SUPPORTED_COUNTRY_CNTRL_ASCII)
			country = 0;		/* Use USA table */

		cntrlkey = &AT_Control[country];
		return CntrlToKeynumber(cntrlkey, ascii);
	}
	if (m_pTerminal->IsCapCntrlDEC())
	{
		cntrlkey = DEC_Control;
		return CntrlToKeynumber(cntrlkey, ascii);
	}
	return 0;
}

/////////////////////////////////////////////////////////////////////////////////////////
// Helper for the Win table

void CWedgeBase::GetCntrlPlusASCII_Win_Keynumber(tichar_t ascii, keynum_t &keynumber, modifiers_t &flags)
{
	int country = GetCountry();
	if (country > MAX_SUPPORTED_COUNTRY_CNTRL_ASCII_WIN)
		country = 0;		// Use USA table

	const CntrlWinCountryDetails_t *pCountry = &CntrlWinTable[country];
	keynumber = pCountry->cntrl[ascii].keynumber;
	flags = pCountry->cntrl[ascii].flags;
}

